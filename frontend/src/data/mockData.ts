export const generateTrackingPoints = (objectId: string, count: number = 50): TrackingPoint[] => {
  const points: TrackingPoint[] = [];
  const baseLat = 40.7128;
  const baseLng = -74.006;
  const now = Date.now();

  for (let i = 0; i < count; i++) {
    points.push({
      id: `track-${objectId}-${i}`,
      objectId,
      location: {
        lat: baseLat + (Math.random() - 0.5) * 0.1,
        lng: baseLng + (Math.random() - 0.5) * 0.1,
        accuracy: 5 + Math.random() * 15,
      },
      timestamp: new Date(now - (count - i) * 10 * 60 * 1000),
      speed: Math.random() * 60,
      heading: Math.random() * 360,
      source: 'GPS',
      metadata: { satelliteCount: 8 + Math.floor(Math.random() * 4) },
    });
  }

  return points;
};

